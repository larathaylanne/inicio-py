# O DESAFIO É CRIAR UM PROGRAMA QUE LEIA UM VALOR EM METROS E EXIBA EM CENTÍMETROS E MILÍMETROS

metros= float(input("Escreva o valor em m²: \n"))

centimetros= metros*100
milimetros= metros*1000

print("Então se liga! \n O valor de ",metros,"² em CENTÍMETROS é:", centimetros,"².\n E o valor em MILÍMETROS é: ",milimetros,"²")
print("\nOBRIGADA!")